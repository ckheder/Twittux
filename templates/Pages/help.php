<!-- Paramétrer mon compte-->

<div>

  <h4 id="accountsettings">Paramétrer mon compte</h4>

  <p>

    Pour paramétrer votre compte, connecter vous à votre compte , cliquez sur <i class="fas fa-users-cog"></i> dans la barre de navigation supérieure et naviguez jusqu'à "Paramètres".<br />
    Vous pouvez défnir votre profil comme public ou privé en cliquant sur la case à cocher de votre choix:

    <ul>

      <li>
        Un profil public accepte automatiquement les abonnements, tous vos tweets sont publics (n'importe qui peut les commenter et les partager, ils sont visibles dans les résultats de recherche). Votre profil est également consultable par les utilisateurs déconnectés ou ne possédant pas de compte Twittux.
      </li>

      <li>
        Un profil privé vous donne le choix de qui peut vous suivre, voir vos tweets (les tweets privés ne se partagent pas), vos tweets n'apparaîtront pas dans le moteur de recherche. Votre profil n'est pas consultable par les utilisateurs déconnectés ou ne possédant pas de compte Twittux.
      </li>

    </ul>

  </p>

</div>

<!-- Problème de connexion ?-->

<div>

  <h4 id="loginproblem">Problème de connexion ?</h4>

  <p>
    Il peut arriver que vous ne puissiez pas accéder à votre compte, voici quelques raisons possibles :

    <ul>

      <li>

        Mauvais mot de passe : Si vous ne vous souvenez plus de votre mot de passe, vous pouvez le réinitialiser à tous moment : pour cela  puis .
        <ul>

          <li>
            Rendez-vous sur la <a href="/twittux" class="w3-text-blue">page d'accueil</a> du site.
          </li>

          <li>
            Cliquez sur "mot de passe oublié ?".
          </li>

          <li>
            Entrez votre adresse mail <u><strong>d'inscription</strong></u> dans la fenêtre qui se sera ouverte.
          </li>

          <li>
            Cliquez sur <button class="w3-button w3-blue w3-round">Envoyer</button>
          </li>

          <li>
            Rendez-vous dans votre messagerie puis cliquez sur le lien inclus dans le message. Attention, le message peut être vu comme un spam (consultez vos spams si vous ne voyez pas le message dans votre boîte de réception).
          </li>

        </ul>

      </li>

      <li>
        Cookies : si les Cookies sont bloqués au niveau de votre navigateur, vous ne pourrez pas vous connecter.En effet, un cookie nécessaire à la sécurité du site est crée à chaque connexion.Pour en savoir plus, <a href="/twittux/privacy" class="w3-text-blue">Politique relative aux cookies</a>.
      </li>

    </ul>

  </p>

</div>

