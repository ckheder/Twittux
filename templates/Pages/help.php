        Attention, vous serez automatiquement déconnecté si vous modifier votre mot de passe afin de vous reconnecter avec le nouveau. Si vous avez modifié d'autres informations et que vous êtes déconnecté pour cause de nouveau mot de passe, elles seront prises en compte.
      </li>

      <li>
        <i class="fas fa-at"></i> Adresse Mail : vous pouvez ici changer d'adresse mail. Assurez vous que celle-ci est valide car elle servira à réinitialiser votre mot de passe en cas d'oubli. Remplissez les deux champs avec la même adresse mail puis cliquez sur le bouton "Mise à jour" un peu plus bas.
